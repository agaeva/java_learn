package ru.stqa.pft.sandbox;

public class SheyhClass {
  public int n;

  public SheyhClass(int n) {
    this.n = n;  }

  public int N() {
    return 2 * this.n + 1;  }

  public int x() {
    return this.n+ 1;  }

  public int y() {
    return (2 * this.n + 1) / 3;  }

  public int z() {
    return (2 * this.n + 1) / 9;  }

}
